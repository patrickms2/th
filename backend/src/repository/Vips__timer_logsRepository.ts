import { EntityRepository, Repository } from 'typeorm';

import { Vips__timer_logs } from '../entity/Vips__timer_logs';
import { applyFilters, EntityQuery } from './vips__timer_logsUtils';

@EntityRepository(Vips__timer_logs)
export class Vips__timer_logsRepository extends Repository<Vips__timer_logs> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__timer_logs[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
