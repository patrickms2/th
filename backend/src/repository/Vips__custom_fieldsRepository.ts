import { EntityRepository, Repository } from 'typeorm';

import { Vips__custom_fields } from '../entity/Vips__custom_fields';
import { applyFilters, EntityQuery } from './vips__custom_fieldsUtils';

@EntityRepository(Vips__custom_fields)
export class Vips__custom_fieldsRepository extends Repository<Vips__custom_fields> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__custom_fields[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
