import { EntityRepository, Repository } from 'typeorm';

import { Vips__invoice_items } from '../entity/Vips__invoice_items';
import { applyFilters, EntityQuery } from './vips__invoice_itemsUtils';

@EntityRepository(Vips__invoice_items)
export class Vips__invoice_itemsRepository extends Repository<Vips__invoice_items> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__invoice_items[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
