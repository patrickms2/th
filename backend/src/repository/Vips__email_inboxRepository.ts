import { EntityRepository, Repository } from 'typeorm';

import { Vips__email_inbox } from '../entity/Vips__email_inbox';
import { applyFilters, EntityQuery } from './vips__email_inboxUtils';

@EntityRepository(Vips__email_inbox)
export class Vips__email_inboxRepository extends Repository<Vips__email_inbox> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__email_inbox[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
