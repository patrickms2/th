import { EntityRepository, Repository } from 'typeorm';

import { Vips__dashboard_settings } from '../entity/Vips__dashboard_settings';
import { applyFilters, EntityQuery } from './vips__dashboard_settingsUtils';

@EntityRepository(Vips__dashboard_settings)
export class Vips__dashboard_settingsRepository extends Repository<Vips__dashboard_settings> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__dashboard_settings[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
