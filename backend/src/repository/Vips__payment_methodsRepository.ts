import { EntityRepository, Repository } from 'typeorm';

import { Vips__payment_methods } from '../entity/Vips__payment_methods';
import { applyFilters, EntityQuery } from './vips__payment_methodsUtils';

@EntityRepository(Vips__payment_methods)
export class Vips__payment_methodsRepository extends Repository<Vips__payment_methods> {

    async filter(query: any | undefined, page: number, size: number, field: string | undefined, sort: "ASC" | "DESC" | undefined = "ASC", countOnly: boolean | undefined ): Promise<(any[] | Promise<number>)[] | Promise<[Vips__payment_methods[], number]>> {
        const qb = this.createQueryBuilder('e');
        applyFilters(qb, query);
        if(field) {
            const dbQuery = qb
              .skip(page * size)
              .take(size)
              .orderBy(`e.${field}`, sort)
              ;

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        } else {
            const dbQuery = qb
              .skip(page * size)
              .take(size)

            return countOnly ? [[], await dbQuery.getCount()] : dbQuery.getManyAndCount();
        }
    }
}
